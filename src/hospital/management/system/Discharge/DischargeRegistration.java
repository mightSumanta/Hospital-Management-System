    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int x = JOptionPane.showConfirmDialog(null, "Confirm registration task ?", "Register Record", JOptionPane.YES_NO_OPTION);
        if (x == 0) {
            try {
                Calendar cal = new GregorianCalendar();
                int month = cal.get(Calendar.MONTH);
                int year = cal.get(Calendar.YEAR);
                int day = cal.get(Calendar.DAY_OF_MONTH);

                String sql = "insert into DischargeSummary "
                        + "(patientId, patientName, admitDate, dischargeDate) "
                        + "values (?, ?, ?, ?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_patientId.getText());
                pst.setString(2, txt_patientName.getText());
                pst.setString(3, txt_admitDate.getText());
                pst.setString(4, txt_dischargeDate.getText());

                pst.execute();
                JOptionPane.showMessageDialog(null, "Data is saved successfully");
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            finally {
                try {
//                   rs.close();
                    pst.close();
                }
                catch (Exception e) {
                   JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

