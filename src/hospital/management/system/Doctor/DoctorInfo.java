    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try {
            String sql = "Select name, department, specialization, contactNo"
                    + " from Doctor where department = ? ";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1, combo_department.getSelectedItem().toString());
            rs = pst.executeQuery();
          
            ResultSetMetaData metaData = rs.getMetaData();
            
            Vector<String> columnNames = new Vector<String>();
            int columnCount  = metaData.getColumnCount();
            
            columnNames.add("Doctor Name");
            columnNames.add("Department");
            columnNames.add("Specialization");
            columnNames.add("Contact No");
            
            Vector<Vector<Object>> data = new Vector<Vector<Object>>();
            while (rs.next()) {
                
                Vector<Object> vector = new Vector<Object>();
                for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                    vector.add(rs.getObject(columnIndex));
                }
                data.add(vector);
            }
            DefaultTableModel tableModel = new DefaultTableModel(data, columnNames);
            table_doctorInfo.setModel(tableModel);
            
            for (int col = 0; col < table_doctorInfo.getColumnCount(); col++) {
                table_doctorInfo.getColumnModel().getColumn(col).setPreferredWidth(200);
            }
            
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something happened");
        }
        finally {
            try {
                pst.close();
                rs.close();
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Something happened, oh God");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

