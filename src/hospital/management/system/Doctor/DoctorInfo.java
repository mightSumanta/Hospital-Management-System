    private void initiateComboBox() {
        
        try {
            String sql = "Select distinct department from Doctor";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while (rs.next()) {
                combo_department.addItem(rs.getString("department"));
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something happened with Combo");
        }
        finally {
            try {
                rs.close();
                pst.close();
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Something happened with Combo, oh God");
            }
        }
    }
